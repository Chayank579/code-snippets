// Sub.java

public class Sub {
    public static void main(String[] args) {
        // Static input values
        double num1 = 15.5;
        double num2 = 7.2;

        // Subtract the numbers
        double result = num1 - num2;

        // Display the result
        System.out.println("The difference between " + num1 + " and " + num2 + " is " + result);
    }
}
